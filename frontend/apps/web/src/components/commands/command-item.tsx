// ============================================================================

export function CommandItem({
  command,
  isSelected = false,
  isFavorite = false,
  onClick,
  onFavoriteToggle,
  showUsage = false,
  showCategory = false,
  className,
}: CommandItemProps) {
  const IconComponent = command.icon ? CommandIcons[command.icon] || DefaultIcon : DefaultIcon

  const handleFavoriteClick = (e: React.MouseEvent) => {
    e.stopPropagation()
    onFavoriteToggle?.()
  }

  return (
    <div
      role="option"
      aria-selected={isSelected}
      tabIndex={0}
      className={cn(
        'flex cursor-pointer items-center gap-3 rounded-md px-3 py-2 transition-colors',
        'hover:text-accent-foreground hover:bg-accent',
        isSelected && 'text-accent-foreground bg-accent',
        className
      )}
      onClick={onClick}
      onKeyDown={(e) => {
        if (e.key === 'Enter' || e.key === ' ') {
          e.preventDefault()
          onClick?.()
        }
      }}
    >
      {/* Command Icon */}
      <div
        className={cn(
          'flex h-8 w-8 items-center justify-center rounded-md',
          'bg-muted text-muted-foreground',
          isSelected && 'bg-primary/10 text-primary'
        )}
      >
        <IconComponent className="h-4 w-4" />
      </div>

      {/* Command Info */}
      <div className="min-w-0 flex-1">
        <div className="flex items-center gap-2">
          <span className="text-sm font-medium">/{command.name}</span>
          {command.aliases && command.aliases.length > 0 && (
            <span className="text-xs text-muted-foreground">
              ({command.aliases.map((a) => `/${a}`).join(', ')})
            </span>
          )}
          {showCategory && (
            <span className="rounded bg-muted px-1.5 py-0.5 text-xs capitalize text-muted-foreground">
              {command.category}
            </span>
          )}
        </div>
        <p className="truncate text-xs text-muted-foreground">{command.description}</p>
        {showUsage && (
          <p className="text-muted-foreground/70 mt-0.5 font-mono text-xs">{command.usage}</p>
        )}
      </div>

      {/* Keyboard Shortcut */}
      {command.shortcut && (
        <kbd className="hidden items-center rounded bg-muted px-1.5 py-0.5 font-mono text-xs text-muted-foreground sm:inline-flex">
          {command.shortcut}
        </kbd>
      )}

      {/* Favorite Button */}
      {onFavoriteToggle && (
        <button
          type="button"
          onClick={handleFavoriteClick}
          className={cn(
            'hover:bg-muted/50 rounded p-1 transition-colors',
            isFavorite
              ? 'text-yellow-500'
              : 'text-muted-foreground opacity-0 group-hover:opacity-100'
          )}
          aria-label={isFavorite ? 'Remove from favorites' : 'Add to favorites'}
        >
          {isFavorite ? (
            <FilledStar className="h-4 w-4" />
          ) : (
            <CommandIcons.star className="h-4 w-4" />
          )}
        </button>
      )}
    </div>
  )
}

